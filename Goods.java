package day8.bookshop;

public class Goods {
	protected String name;
	protected int price;

	public Goods() {
	}

	
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

}
